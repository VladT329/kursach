public class Employee {
    String surname;
    String name;
    String patronymic;
    String department;
    int salary;

    public Employee (String surname, String name, String patronymic, String department, int salary){
        this.surname = surname;
        this.name = name;
        this.patronymic = patronymic;
        this. department = department;
        this. salary = salary;

    }
}
